if (localStorage.getItem("modal") === "true") {
    $("landing-modal").css("display", "none");
} else if (localStorage.getItem("modal") === null) {
    $("#landing-modal").modal({
        fadeDuration: 500,
    });
    localStorage.setItem("modal", "true");
}

$("#landing-modal").modal({
    fadeDuration: 500,
});
